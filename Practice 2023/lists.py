
### DEFINE LIST ###

TheList = ["pie", "cake", "cheese cake"]
print(TheList)

# add to list # 
TheList.append("cookie")
    
print(TheList)

# remove from list # 

TheList.remove("pie")
print(TheList)

# remove from the last of the list # 

TheList.pop()
print(TheList)

# clear from list #

TheList.clear()
print(TheList)